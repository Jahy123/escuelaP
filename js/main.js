const contenedorMultimedia = document.getElementById("contenedorMultimedia");

multimedia.forEach((item) => {
  const div = document.createElement("div");

  if (item.type === "imagen") {
    const img = document.createElement("img");
    div.className = "imagen";
    img.src = item.media;
    img.alt = item.name;
    div.appendChild(img);
  } else if (item.type === "video") {
    const video = document.createElement("video");
    div.className = "video";
    video.id = "video" + item.id; // Agregado "video" como prefijo para hacer el ID único
    video.src = item.media;
    video.alt = item.name;
    video.controls = true;
    div.appendChild(video);
  }

  contenedorMultimedia.appendChild(div);

  const divSeparador = document.createElement("div");
  const parrafo = document.createElement("p");
  divSeparador.className = "parrafo";
  parrafo.textContent = item.descripcion;
  divSeparador.appendChild(parrafo);
  contenedorMultimedia.appendChild(divSeparador);
});

// Obtén referencias a los elementos de video y audio
const videos = document.querySelectorAll("video");
const audioElement = document.getElementById("audio1");

// Escucha el evento de reproducción de cada video
videos.forEach((video) => {
  video.addEventListener("play", function () {
    // Pausa el audio si se está reproduciendo
    if (!audioElement.paused) {
      audioElement.pause();
    }
  });

  // Escucha el evento de pausa de cada video
  video.addEventListener("pause", function () {
    // Reproduce el audio si estaba pausado
    if (audioElement.paused) {
      audioElement.play();
    }
  });
});
